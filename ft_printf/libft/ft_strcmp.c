#include "libft.h"

char	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i] != '\0')
	{
		if (*s1 == s2[i])
		{
			return (s2[i]);
		}
		i++;
	}
	return (0);
}
